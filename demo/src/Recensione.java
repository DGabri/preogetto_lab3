package demo.src;

public class Recensione {
    public int posizione;
    public int pulizia;
    public int servizio;
    public int prezzo;
    
    public Recensione(int posizione, int pulizia, int servizio, int prezzo) {
        this.posizione = posizione;
        this.pulizia = pulizia;
        this.servizio = servizio;
        this.prezzo = prezzo;
    }
}
